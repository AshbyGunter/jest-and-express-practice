
const add = (x, y) => {
  return x + y;
}

const multiply = (x, y) => {
  return x * y;
}

const divide = (x, y) => {
  return x / y;
}

const subtract = (x, y) => {
  return x - y;
}

const exponential = (x, y) => {
  return x ** y;
  // return x * y;
}

const addThree = (x, y, z) => {
  return x + y + z;
}

exports.add = add;
exports.multiply = multiply;
exports.divide = divide;
exports.subtract = subtract;
exports.exponential = exponential;
exports.addThree = addThree;